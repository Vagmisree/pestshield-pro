export function getServiceBySlug(slug: string): Service | undefined {
  return services.find(service => service.slug === slug)
}

export function getServicesByTag(tag: Service['tag'] | 'Organic' | 'All'): Service[] {
  if (tag === 'All') return services
  if (tag === 'Organic') return services.filter(s => s.chemicalType === 'Organic')
  return services.filter(s => s.tag === tag || s.tag === 'Both')
}
